    private final static String USER_NOT_FOUND_MSG = "user with email %s not found";
    private final UserRepository userRepository;
    private final EmailValidator emailValidator;
    private final ConfirmationTokenService confirmationTokenService;
    private final BCryptPasswordEncoder bCryptPasswordEncoder;
    private final EmailSender emailSender;

    @Override
    public UserDetails loadUserByUsername(String email) throws UsernameNotFoundException {
        return userRepository.findByEmail(email)
                .orElseThrow(() -> new UsernameNotFoundException(String.format(USER_NOT_FOUND_MSG, email)));
    }

    public String register(Users users){
        boolean isValidEmail = emailValidator.test(users.getEmail());

        if (!isValidEmail){
            throw new IllegalStateException("email not valid");
        }

        boolean userExists = userRepository.findByEmail(users.getEmail()).isPresent();

        if (userExists){
            // TODO check of attributes are the same and
            // TODO if email not confirmed send confirmation email.
            throw new IllegalStateException("email already taken");
        }

        String encodedPassword = bCryptPasswordEncoder.encode(users.getPassword());

        users.setPassword(encodedPassword);
        users.setUserRole(UserRole.USER);

        userRepository.save(users);

        String token = UUID.randomUUID().toString();

        ConfirmationToken confirmationToken = new ConfirmationToken(token, LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15), users);

        confirmationTokenService.saveConfirmationToken(confirmationToken);

        String link = "http://localhost:8080/api/user/confirm?token=" + token;
        emailSender.send(users.getEmail(), buildEmail(users.getFirstName(), link));

        return token;
    }

    @Transactional
    public String confirmToken(String token){
        ConfirmationToken confirmationToken = confirmationTokenService.getToken(token)
                .orElseThrow(() -> new IllegalStateException("token not found"));

        if (confirmationToken.getConfirmedDate() != null){
            throw new IllegalStateException("email already confirmed");
        }

        LocalDateTime expiredDate = confirmationToken.getExpiredDate();

        if (expiredDate.isBefore(LocalDateTime.now())){
            throw new IllegalStateException("token expired");
        }

        confirmationTokenService.setConfirmedDate(token);
        userRepository.enableAppUser(confirmationToken.getUsers().getEmail());

        return "confirmed";
    }

